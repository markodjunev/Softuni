namespace Operations
{

    public class MathOperations
    {
        internal int Add(int a, int b)
        {
            return a + b;
        }

        internal double Add(double a, double b, double c)
        {
            return a + b + c;
        }

        internal decimal Add(decimal a, decimal b, decimal c)
        {
            return a + b + c;
        }
    }
}